package day07_Operators;

public class ShortHandOperators {
    public static void main(String[] args) {
        int number = 100;

        System.out.println("number= "+number);

        number = 200;
        System.out.println("number = " + number);

        String word = "java programming";
        System.out.println("word = " + word); // java programming

        word = "wooden spoon";
        System.out.println("word = " + word);// "wooden spoon" now we have updated the new value or new string

        word = "Cydeo";
        System.out.println("word = " + word);// "Cydeo" whenever you assign a new value its going to take the new value



    }
}
