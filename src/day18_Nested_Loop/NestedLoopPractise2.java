package day18_Nested_Loop;

public class NestedLoopPractise2 {
    public static void main(String[] args) {

        for (int i = 1; i < 11; i++) {
            for (int j = 0; j < 10; j++) {
                System.out.print("* ");
            }
            System.out.println();

        }




    }
}
